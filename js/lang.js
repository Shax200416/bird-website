const flagImages = {
  en: "img/flagsen.png",
  ru: "img/flagsru.png",
  kg: "img/flagskg.png"
};

function updateCurrentLangButton(lang){
  const currentBtn = document.getElementById("current-lang");
  if(!currentBtn) return;
  currentBtn.querySelector("img").src = flagImages[lang];
  currentBtn.querySelector("span").textContent = lang.toUpperCase();
}

function translatePage(lang){
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const key = el.getAttribute("data-i18n");
    if(translations[lang] && translations[lang][key]) el.textContent = translations[lang][key];
  });

  document.querySelectorAll("[data-i18n-btn]").forEach(el=>{
    const key = el.getAttribute("data-i18n-btn");
    if(translations[lang] && translations[lang][key]) el.textContent = translations[lang][key];
  });

  const search = document.getElementById("searchInput");
  if(search) search.placeholder = translations[lang].search_placeholder;
}

function translateFilters(lang){
  const dietSelect = document.getElementById("dietFilter");
  const habitatSelect = document.getElementById("habitatFilter");

  if(dietSelect){
    Array.from(dietSelect.options).forEach(opt=>{
      if(opt.value === "") {
        opt.textContent = translations[lang].diet_all; 
      } else if(translations[lang][opt.value]){
        opt.textContent = translations[lang][opt.value];
      }
    });
  }

  if(habitatSelect){
    Array.from(habitatSelect.options).forEach(opt=>{
      if(opt.value === "") {
        opt.textContent = translations[lang].habitat_all; 
      } else if(translations[lang][opt.value]){
        opt.textContent = translations[lang][opt.value];
      }
    });
  }

  const clearBtn = document.getElementById("clearFilters");
  if(clearBtn) clearBtn.textContent = translations[lang].clear;
}

function setLanguage(lang){
  if(!translations[lang]) return;
  if(typeof loadBanner === 'function') loadBanner();
  if(typeof loadFeatured === "function") loadFeatured();

    localStorage.setItem("lang", lang);
    translatePage(lang);
    translateFilters(lang);
    updateCurrentLangButton(lang);

    const dropdown = document.getElementById("lang-dropdown");
    if(dropdown) dropdown.classList.remove("active");

    const overlay = document.getElementById("overlay");
    if(overlay) overlay.classList.remove("active");

    if(typeof applyFilters === "function") applyFilters();
    if(typeof renderBirdDetail === "function") renderBirdDetail();
    if(typeof loadBanner === "function") loadBanner();
    if(typeof showFact === "function") showFact();
    if(typeof loadFeatured === "function") loadFeatured();
    if(typeof loadBotd === "function") loadBotd();
    if(typeof prepareQuiz === "function"){ 
        prepareQuiz();
        quizIndex = 0;
        quizScore = 0;
        loadQuiz();
    }
}

document.addEventListener("DOMContentLoaded", ()=>{
  const currentBtn = document.getElementById("current-lang");
  const dropdown = document.getElementById("lang-dropdown");
  const overlay = document.getElementById("overlay");

  if(currentBtn && dropdown && overlay){
    currentBtn.addEventListener("click", ()=>{
      dropdown.classList.toggle("active");
      overlay.classList.toggle("active");
    });

    overlay.addEventListener("click", ()=>{
      dropdown.classList.remove("active");
      overlay.classList.remove("active");
    });
  }

  document.querySelectorAll("#lang-dropdown .lang-btn").forEach(btn=>{
    btn.addEventListener("click", ()=> setLanguage(btn.dataset.lang));
  });

  const savedLang = localStorage.getItem("lang") || "en";
  setLanguage(savedLang);

  document.addEventListener("click", e=>{
    if(!currentBtn.contains(e.target) && !dropdown.contains(e.target)){
      dropdown.classList.remove("active");
      overlay.classList.remove("active");
    }
  });
});